    public UserDTO retriveUserInfo() {
        String username = usernameTxt.getText();
        String password = new String(passwordTxt.getPassword());
        if (password.equals("")) {
            JOptionPane.showMessageDialog(this, "Password must not be null!");
            return null;
        }
        String fullname = fullnameTxt.getText();
        if (fullname.equals("")) {
            JOptionPane.showMessageDialog(this, "Full name must not be null!");
            return null;
        }
        String role = user.roles;
        String passhint = passhintTxt.getText();
        String avatar = avatarLink;
        return new UserDTO(username, password, fullname, role, passhint, avatar);
    }
    
    public void distriveUserInfo() {
        String[] names = user.fullname.split(" ");
        titleLbl.setText(names[names.length-1] + "'s profile");
        usernameTxt.setText(user.username);
        passwordTxt.setText(user.password);
        fullnameTxt.setText(user.fullname);
        passhintTxt.setText(user.passhint);
        avatarLink = user.avatar;
        ImageLoader.smartAttaching(avatarLink, avatarLbl);
        ImageLoader.smartAttaching(avatarLink, logoLabel);
    }
    
